  var NEP_DIGITS = ["०","१","२","३","४","५","६","७","८","९"];

  function toNepDigits(n) {
    return String(n).replace(/\d/g, function (d) { return NEP_DIGITS[+d]; });
  }

  function adToBs(input) {
    var ad = (input instanceof Date) ? input : new Date(input + "T00:00:00Z");
    var adUTC = Date.UTC(ad.getUTCFullYear(), ad.getUTCMonth(), ad.getUTCDate());
    var days = Math.floor((adUTC - REF_AD) / 86400000);
    if (days < 0) return null;
    var y = 0;
    while (y < BS_DATA.length) {
      var yearDays = BS_DATA[y].reduce(function (a, b) { return a + b; }, 0);
      if (days < yearDays) break;
      days -= yearDays;
      y++;
    }
    if (y >= BS_DATA.length) return null;
    var m = 0;
    while (m < 12 && days >= BS_DATA[y][m]) { days -= BS_DATA[y][m]; m++; }
    return { y: BS_START_YEAR + y, m: m + 1, d: days + 1 };
  }

  function formatBsNep(input) {
    var b = adToBs(input);
    if (!b) return "";
    return toNepDigits(b.d) + " " + NEP_MONTHS[b.m - 1] + " " + toNepDigits(b.y);
  }
  function formatBsDayMonth(input) {
    var b = adToBs(input);
    if (!b) return "";
    return toNepDigits(b.d) + " " + NEP_MONTHS[b.m - 1];
  }
